<a name="scan">Een barcodelezer gebruiken:

Elke barcodelezer met toetsenbordemulatie wordt ondersteund door OpenBiblio.</a> Dit kunnen USB scanners 
and scanners die tusen computer en toetsenbord gezet worden (keyboard wedge).<br>
De meeste barcodelezers kunnen geprogrammeerd worden om automatisch een carriage return (hetzelfde als ENTER drukken op het toetsenbord) uit te voeren na elke scan. Op deze manier zal OpenBiblio je opdracht onmiddellijk verwerken als je een item scant.
