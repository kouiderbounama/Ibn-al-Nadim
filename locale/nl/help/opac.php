<br><br>
<a name="sec2">Als resultaat van een zoekopdracht krijg je een lijst met van elk object de titel, de auteur, de aard van het materiaal (boek, DVD, ...), de collectie waaruit het object komt, het ruglabel, de barcode en de status (uitgeleend of aanwezig).</a>
