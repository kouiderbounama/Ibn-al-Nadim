<h1>Kort overzicht voor snelle starters</h1>
<h2>Boeken terugnemen</h2>
<ul>
<li>Klik up Terugnemen/Uitlenen, Terugnemen</li>
<li>Scan de terug te nemen boeken, de boeken komen 'op de kar'.</li>
<li>Nadat de boeken terug op het rek liggen, klik je op 'Alle items terug op hun plaats'</li>
</ul>
<h2>Boeken ontlenen</h2>
<ul>
<li>Klik op Terugnemen/Uitlenen,  Lid zoeken</li>
<li>Scan de barcode van de ontlener of zoek op achternaam indien kaart niet bij.</li>
<li>Scan de barcodes van de boeken.</li>
</ul>

<h2>Nieuwe boeken</h2>
<ul>
<li>Klik op Catalogus, Nieuw object</li>
<li>Vul minstens de velden met sterretje in.<br />
<em>Let op: ruglabel bovenste lijntje zijn de eerste vier letters van de auteur, tweede lijntje is het genre.</em></li>
<li>Klik op Bevestig. De bibliografie (Biblioinfo) is nu aangemaakt, maar er zitten nog geen boeken van in de bibliotheek.</li>
<li>Klik in het volgende scherm op Toevoegen nieuw exemplaar</li>
<li>Scan of typ de barcode van het boek. (automatisch genereren alleen gebruiken als je de interne nummering van OpenBiblio gebruikt!).<br />
<em>Omschrijving is een optioneel veld. Zet hier maar iets als er wat te melden is over dit specifiek exemplaar.</em></li>
<li>Klik op bevestig (niet nodig met scanner - gebeurt automatisch. Het boek zit nu in de bibliotheek. Je kunt nog meerdere exemplaren toevoegen met dezelfde bibliografie door terug op Toevoegen nieuw exemplaar te klikken.</li>
</ul>








