  <li><a href="#sec1">Terugnemen</a></li>
  <li><a href="#sec2">Op de kar</a></li>
  <li><a href="#sec3">Zoeken zonder barcode</a></li>
</ul>
<a name="sec1"></a>
<p>Terugnemen van boeken doe je via deze pagina. Je scant het boek in en klikt op 'Op de kar'. Als je barcodelezer geprogrammeerd is met een 'enter' na de gelezen code, gebeurt dit automatisch. Het boek komt dan 'op de kar', klaar om terug in de rekken te zetten. Het boek is al wel terug beschikbaar om uitgeleend te worden.</p>
<a name="sec2"></a>
<p>De normale procedure is dan dat de boeken teruggezet worden en dat je dan klikt op 'Alle items terug op hun plaats' (de links boven en onder de boekenlijst op de kar doen hetzelfde).</p>
<p>Je kunt ook een aantal boeken terugzetten en een aantal niet door de boeken die je wil terugzetten aan te vinken en de link 'Geselecteerde items terug op hun plaats' te gebruiken.</p>
<a name="sec3"></a>
<p>De link 'Zoeken' opent de OPAC in een nieuw scherm. Daar kun je boeken zoeken op auteur, titel of onderwerp. Door te klikken op terugnemen/uitlenen komt de barcode in het formulierveld te staan, zodat het boek teruggenomen of uitgeleend kan worden zonder dat de barcode gekend is. Via dit scherm kan een boek ook gereserveerd worden.</p>



