<h1>Bibliografie: nieuw- en bewerkpagina's:</h1>
De velden op de bibliografiepagina zijn gebaseerd op de  <a href="javascript:popSecondaryLarge('http://www.loc.gov/marc')">MARC 21</a> standaard.  Slechts eeen heel kleine subset van de vele honderden MARC-velden zijn opgenomen op deze pagina om het gemakkelijk te maken om boeken toe te voegen. Als een boek is toegevoegd, kun je ander MARC 21-velden toevoegen met de bewerk-MARC-pagina.
<br><br>
Als je een nieuw boek hebt toegevoegd, moet je er ook minstens één exemplaar van toevoegen voor je ze kunt uitlenen.
